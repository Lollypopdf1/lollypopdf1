            const mobileMenuOverlay = document.getElementById('mobile-menu-overlay');
            const mobileMenuClose = document.getElementById('mobile-menu-close');
            const mobileSearchButton = document.getElementById('mobile-search-button');
            const mobileSearch = document.getElementById('mobile-search');
            const mobileSearchInput = document.getElementById('mobile-search-input');
            const mobileSearchResults = document.getElementById('mobile-search-results');
            const desktopSearchInput = document.getElementById('desktop-search');
            const desktopSearchResults = document.getElementById('desktop-search-results');



            // Search functionality
            function performSearch(query, resultsContainer) {
                if (query.length < 2) {
                    resultsContainer.classList.add('hidden');
                    return;
                }

                const filteredTools = tools.filter(tool =>
                    tool.name.toLowerCase().includes(query.toLowerCase()) ||
                    tool.category.toLowerCase().includes(query.toLowerCase())
                );

                if (filteredTools.length === 0) {
                    resultsContainer.innerHTML = '<div class="search-no-results">No tools found</div>';
                    resultsContainer.classList.remove('hidden');
                    return;
                }

                const resultsHTML = filteredTools.slice(0, 8).map(tool => `
                    <div class="search-result-item" onclick="window.location.href='${basePath}${tool.url}'">
                        <div class="search-result-title">${tool.name}</div>
                        <div class="search-result-category">${tool.category}</div>
                    </div>
                `).join('');

                resultsContainer.innerHTML = resultsHTML;
                resultsContainer.classList.remove('hidden');
            }

            // Mobile search functionality
            if (mobileSearchButton && mobileSearch) {
                mobileSearchButton.addEventListener('click', function() {
                    // Close mobile menu if open
                    if (mobileMenu && mobileMenu.classList.contains('active')) {
                        closeMobileMenu();
                    }

                    // Toggle search
                    mobileSearch.classList.toggle('active');
                    mobileSearchButton.classList.toggle('active');

                    // Update search button icon
                    const searchIcon = mobileSearchButton.querySelector('i');
                    if (mobileSearch.classList.contains('active')) {
                        searchIcon.className = 'fas fa-times text-xl';
                        setTimeout(() => mobileSearchInput.focus(), 100);
                    } else {
                        searchIcon.className = 'fas fa-search text-xl';
                        mobileSearchResults.classList.add('hidden');
                    }
                });

                // Mobile search input
                mobileSearchInput.addEventListener('input', function() {
                    performSearch(this.value, mobileSearchResults);
                });

                // Clear search results when input is cleared
                mobileSearchInput.addEventListener('blur', function() {
                    setTimeout(() => {
                        if (!this.value) {
                            mobileSearchResults.classList.add('hidden');
                        }
                    }, 150);
                });
            }

            // Desktop search functionality
            if (desktopSearchInput) {
                desktopSearchInput.addEventListener('input', function() {
                    performSearch(this.value, desktopSearchResults);
                });

                desktopSearchInput.addEventListener('blur', function() {
                    setTimeout(() => {
                        if (!this.value) {
                            desktopSearchResults.classList.add('hidden');
                        }
                    }, 150);
                });
            }

            // Mobile slide menu functionality
            function openMobileMenu() {
                console.log('Opening mobile menu...');
                console.log('Mobile menu element:', mobileMenu);
                console.log('Mobile menu overlay element:', mobileMenuOverlay);
                
                if (mobileMenuOverlay && mobileMenu) {
                    mobileMenuOverlay.classList.add('active');
                    mobileMenu.classList.add('active');
                    document.body.style.overflow = 'hidden'; // Prevent background scrolling
                    
                    // Debug: Check if classes were added
                    console.log('Menu classes after adding active:', mobileMenu.className);
                    console.log('Overlay classes after adding active:', mobileMenuOverlay.className);
                    
                    // Force the menu to be visible for testing
                    mobileMenu.style.right = '0px';
                    mobileMenu.style.backgroundColor = 'red';
                } else {
                    console.error('Mobile menu elements not found!');
                }
            }
            
            function closeMobileMenu() {
                console.log('Closing mobile menu...');
                mobileMenuOverlay.classList.remove('active');
                mobileMenu.classList.remove('active');
                document.body.style.overflow = ''; // Restore scrolling
                
                // Reset the forced styles
                mobileMenu.style.right = '';
                mobileMenu.style.backgroundColor = '';
            }
            
            if (mobileMenuButton && mobileMenu && mobileMenuOverlay) {
                mobileMenuButton.addEventListener('click', function(e) {
                    e.preventDefault();
                    e.stopPropagation();
                    

                    
                    // Close mobile search if open
                    if (mobileSearch && mobileSearch.classList.contains('active')) {
                        mobileSearch.classList.remove('active');
                        mobileSearchButton.classList.remove('active');
                        const searchIcon = mobileSearchButton.querySelector('i');
                        searchIcon.className = 'fas fa-search text-xl';
                        mobileSearchResults.classList.add('hidden');
                    }

                    openMobileMenu();
                });
                
                // Close menu when clicking close button
                if (mobileMenuClose) {
                    mobileMenuClose.addEventListener('click', closeMobileMenu);
                }
                
                // Close menu when clicking overlay
                mobileMenuOverlay.addEventListener('click', closeMobileMenu);
                
                // Close mobile menu when clicking on menu links
                const mobileMenuLinks = mobileMenu.querySelectorAll('a');
                mobileMenuLinks.forEach(link => {
                    link.addEventListener('click', function() {
                        closeMobileMenu();
                    });
                });
                
                // Close menu on escape key
                document.addEventListener('keydown', function(event) {
                    if (event.key === 'Escape' && mobileMenu.classList.contains('active')) {
                        closeMobileMenu();
                    }
                });
            }
                
            // Close search and menu when clicking outside
                document.addEventListener('click', function(event) {
                // Close mobile menu (handled by overlay click)
                // The overlay click is already handled in the mobile menu functionality

                // Close mobile search
                if (mobileSearchButton && mobileSearch && 
                    !mobileSearchButton.contains(event.target) && 
                    !mobileSearch.contains(event.target)) {
                    if (mobileSearch.classList.contains('active')) {
                        mobileSearch.classList.remove('active');
                        mobileSearchButton.classList.remove('active');
                        
                        const searchIcon = mobileSearchButton.querySelector('i');
                        searchIcon.className = 'fas fa-search text-xl';
                        mobileSearchResults.classList.add('hidden');
                    }
                }

